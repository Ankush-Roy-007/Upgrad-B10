package JavaPraticeProgram.CommandLineExample.Lecture9;

public class Addition {
    public void add(int a , int b){
        int sum = a + b;
        System.out.println(""+a+" + "+b+" = "+sum);
    }

    public void add(double a , double b){
        double sum = a + b;
        System.out.println(""+a+" + "+b+" = "+sum);
    }
}
