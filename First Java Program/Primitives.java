package com.soham;

public class Primitives {
    public static void main(String[] args) {
        // Primitive Datatypes -
        // Store actual values directly in memory.
        // Value cannot be further broken down.

        // int - Stores normal whole numbers
        int rollNo = 38;

        // char - Stores a single character
        char grade = 'A';

        // float - Stores decimal values (less accurate)
        float marks = 98.67f;

        // double - Stores decimal values (more accurate)
        double pi = 3.14;

        // long - Stores very large whole numbers
        long population = 31803030313L;

        // short - Stores small whole numbers
        short amount = 15000;

        // byte - Stores very small whole numbers
        byte age = 22;

        // boolean - Stores true or false values
        boolean isActive = true;
    }
}
